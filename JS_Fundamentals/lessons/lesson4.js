// Relational or Comparison operators

// >, <, >=, <=
var result = 10 > 5
console.log(result)

result = 4 > 5
console.log(result)

// Equalit operators
var x = 1
console.log(x == "1") // lose comparison: no data type comparison
console.log(x === "1") // strict comparison: not only checking the value, but also checking the data type
